alter table public.links
  add column if not exists inactive_reason text;

comment on column public.links.inactive_reason is
  'Motivo técnico controlado para desativação. Não armazenar texto livre do usuário.';

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'links_inactive_reason_check'
      and conrelid = 'public.links'::regclass
  ) then
    alter table public.links
      add constraint links_inactive_reason_check
      check (
        inactive_reason is null
        or inactive_reason in (
          'reports_threshold',
          'deduplicated_active_link',
          'manual_review'
        )
      ) not valid;
  end if;
end;
$$;

-- Mantém o link ativo mais recente de cada turma. Empates são resolvidos por id.
-- Nenhum registro é excluído; os excedentes são desativados e identificados.
with ranked_active_links as (
  select
    id,
    row_number() over (
      partition by turma_id
      order by created_at desc, id desc
    ) as active_rank
  from public.links
  where is_active is true
)
update public.links as links
set
  is_active = false,
  inactive_reason = coalesce(links.inactive_reason, 'deduplicated_active_link')
from ranked_active_links
where links.id = ranked_active_links.id
  and ranked_active_links.active_rank > 1;

create unique index if not exists one_active_link_per_class
  on public.links (turma_id)
  where is_active is true;

alter table public.links validate constraint links_inactive_reason_check;

