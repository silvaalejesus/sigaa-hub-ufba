-- -----------------------------------------------------------------------------
-- Eventos persistentes de rate limiting
-- -----------------------------------------------------------------------------

create table if not exists public.abuse_events (
  id bigint generated by default as identity primary key,
  action_scope text not null,
  reporter_fingerprint text not null,
  resource_id uuid,
  outcome text not null,
  created_at timestamptz not null default now(),
  constraint abuse_events_action_scope_check
    check (
      action_scope in (
        'add_link_attempt',
        'add_link_success',
        'report_link_attempt',
        'report_link_success'
      )
    ),
  constraint abuse_events_fingerprint_format
    check (reporter_fingerprint ~ '^[0-9a-f]{64}$'),
  constraint abuse_events_outcome_check
    check (
      outcome in (
        'attempted',
        'accepted',
        'rate_limited',
        'duplicate',
        'inactive',
        'not_found',
        'active_link_exists'
      )
    )
);

comment on table public.abuse_events is
  'Eventos pseudonimizados para rate limiting. Retenção operacional de 30 dias.';
comment on column public.abuse_events.reporter_fingerprint is
  'HMAC SHA-256 calculado no servidor. Nunca contém o IP bruto.';
comment on column public.abuse_events.resource_id is
  'Identificador técnico opcional do recurso; não contém URL nem conteúdo do formulário.';

create index if not exists abuse_events_scope_fingerprint_created_at_idx
